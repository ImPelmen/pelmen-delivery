CREATE TABLE IF NOT EXISTS domain_object (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    address VARCHAR(1000) NOT NULL,
    is_apartment BOOLEAN NOT NULL DEFAULT TRUE,
    user_id BIGINT NOT NULL,
    region_id BIGINT NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_domain_object_domain_user FOREIGN KEY (user_id) REFERENCES domain_user(id),
    CONSTRAINT fk_domain_object_region FOREIGN KEY (region_id) REFERENCES region(id)
);